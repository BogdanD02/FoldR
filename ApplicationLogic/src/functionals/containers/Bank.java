package functionals.containers;

/**
 * This class models a bank
 * <p>
 * This class holds all the necessary data about
 * banks.
 * </p>
 * 
 * @version 1.0.0
 * @author David Bogdan
 * @since 1.0.0
 */
public final class Bank {
	/**
	 * The name of the Bank
	 */
	public String Name;
	
	/**
	 * The account name
	 */
	public String Acc;
	
	/**
	 * The coin for the account (e.g. RON, EUR, USD, ..)
	 */
	public String Coin;
	
	/**
	 * The bank's city
	 */
	public String Loc;
	
	public Bank() {
		
	}

}
